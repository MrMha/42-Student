#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*p;

	i = 0;
	p = (char *)s;
	while (p[i] != c && p[i] != '\0')
	{
		i++;
		if (p[i] == c)
		{
			return (&p[i]);
		}
	}
	return (0);
}
