#include "libft.h"

void	bzero(void *s, size_t n)
{
	size_t	i;
	char	*p;

	i = 0;
	p = (char *)s;
	while (p[i] && i < n)
	{
		p[i] = '\0';
		i++;
	}
	return (p);
}
